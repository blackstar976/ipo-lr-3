# Задаем правильный пароль в виде строки
correct_password = "black7"

# Запрашиваем у пользователя ввод пароля
# Функция input() выводит приглашение и возвращает введенную строку
user_password = input("Введите пароль: ")

# Проверяем, совпадает ли введенный пароль с правильным паролем
# Оператор == сравнивает две строки на идентичность
if user_password == correct_password:
    # Если пароли совпадают, выводим сообщение о разрешении доступа
    print("Доступ разрешен")
else:
    # Если пароли не совпадают, выводим сообщение о запрете доступа
    print("Доступ запрещен")