# Запрашиваем у пользователя день месяца и преобразуем в целое число
day = int(input("Введите день: "))

# Запрашиваем у пользователя номер месяца и преобразуем в целое число
month = int(input("Введите месяц (числом от 1 до 12): "))

# Проверяем корректность введенной даты
if (month < 1 or month > 12) or (day < 1 or day > 31):
    # Если месяц не в диапазоне 1-12 или день не в диапазоне 1-31, выводим сообщение об ошибке
    print("Ошибка: введена некорректная дата")
else:
    # Проверяем, относится ли дата к весеннему сезону (март-май)
    if (month == 3 and day >= 1) or (month == 4) or (month == 5) or (month == 6 and day == 0):
        # Весна: с 1 марта по 31 мая
        # Условие: месяц 3 и день >=1, ИЛИ месяц 4, ИЛИ месяц 5, ИЛИ месяц 6 и день 0 (не существует, для корректности)
        # Фактически: март (с 1 числа), апрель, май полностью
        print("Весна")
    
    # Проверяем, относится ли дата к летнему сезону (июнь-август)
    elif (month == 6 and day >= 1) or (month == 7) or (month == 8) or (month == 9 and day == 0):
        # Лето: с 1 июня по 31 августа
        # Условие: месяц 6 и день >=1, ИЛИ месяц 7, ИЛИ месяц 8, ИЛИ месяц 9 и день 0 (не существует)
        # Фактически: июнь (с 1 числа), июль, август полностью
        print("Лето")
    
    # Проверяем, относится ли дата к осеннему сезону (сентябрь-ноябрь)
    elif (month == 9 and day >= 1) or (month == 10) or (month == 11) or (month == 12 and day == 0):
        # Осень: с 1 сентября по 30 ноября
        # Условие: месяц 9 и день >=1, ИЛИ месяц 10, ИЛИ месяц 11, ИЛИ месяц 12 и день 0 (не существует)
        # Фактически: сентябрь (с 1 числа), октябрь, ноябрь полностью
        print("Осень")
    
    # Если ни одно из предыдущих условий не выполнилось, значит дата относится к зиме
    else:
        # Зима: с 1 декабря по 28/29 февраля
        # Оставшиеся месяцы: декабрь, январь, февраль
        print("Зима")