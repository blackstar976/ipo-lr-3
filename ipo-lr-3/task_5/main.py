# Запрашиваем у пользователя число и преобразуем ввод в тип float для работы с дробными числами
number = float(input("Введите число: "))

# Используем вложенные тернарные выражения для определения знака числа
# Первый уровень: проверяем равно ли число нулю
# Если число равно 0, возвращаем "Ноль"
# Если число не равно 0, переходим ко второму уровню проверки
# Второй уровень: проверяем больше ли число нуля
# Если число > 0, возвращаем "Положительное", иначе "Отрицательное"
result = "Ноль" if number == 0 else "Положительное" if number > 0 else "Отрицательное"

# Выводим результат определения знака числа
print(result)