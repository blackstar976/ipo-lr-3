# Запрашиваем два числа у пользователя
num1 = float(input("Введите первое число: "))
num2 = float(input("Введите второе число: "))
# Используем тернарное выражение для вывода результата
# Если числа равны - выводим сообщение о равенстве, иначе находим и выводим минимальное число
print("Числа равны" if num1 == num2 else f"Наименьшее число: {num1 if num1 < num2 else num2}")